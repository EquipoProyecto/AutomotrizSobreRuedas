 void acceder(String usuario, String password)
    {
        String cap="";
         String sql = "SELECT * FROM perfiles WHERE usuario='"+usuario+"' && password ='"+password+"'";
        try {
            Statement st = cn.createStatement();
            ResultSet rs= st.executeQuery(sql);
            while(rs.next())
            {
               cap=rs.getString("perfil");
            }
               if(cap.equals("Cliente"))
               {
                   this.setVisible(false);
                   JOptionPane.showMessageDialog(null,"Bienvenido: "+usuario);
                   Cliente cli = new Cliente();
                   cli.setVisible(true);
                   cli.pack();
               }
              else if(cap.equals("Vendedor"))
               {
                   this.setVisible(false);
                   JOptionPane.showMessageDialog(null,"Bienvenido: "+usuario);
                   Vendedor ven = new Vendedor();
                   ven.setVisible(true);
                   ven.pack();
               }
                else if(cap.equals("Supervisor"))
               {
                   this.setVisible(false);
                   JOptionPane.showMessageDialog(null,"Bienvenido: "+usuario);
                   Supervisor sup = new Supervisor();
                   sup.setVisible(true);
                   sup.pack();
               }
                else if((!cap.equals("Cliente"))&&(!cap.equals("Vendedor"))&&(!cap.equals("Supervisor")))
               {
                  JOptionPane.showMessageDialog(this,"No existen sus datos");
               }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
